#!/usr/bin/env node

/**
 * Generates terminal-content.ts from project files.
 * Run this before build to update the embedded content.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const outputFile = path.resolve(
  __dirname,
  "../app/components/terminal-content.ts"
);

// Read project files
const readme = fs.readFileSync(path.join(projectRoot, "README.md"), "utf-8");
const packageJson = JSON.parse(
  fs.readFileSync(path.join(projectRoot, "package.json"), "utf-8")
);

// Escape backticks and ${} for template literals
function escapeTemplate(str) {
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const content = `// Auto-generated by scripts/generate-terminal-content.mjs
// Do not edit manually - run "pnpm generate" to regenerate

export const version = "${packageJson.version}";

// Command outputs
export const CMD_ABOUT = \`pi-agent-just v${packageJson.version}
Browser-based AI coding agent sandbox.
Terminal on the right, Code Studio on the left.

Built on just-bash for secure sandboxed execution
and pi-coding-agent for the AI agent runtime.

Commands:
  about       About this project
  github      GitHub repository

Or chat with the AI agent naturally.
\`;

export const CMD_INSTALL = \`pnpm install

Usage:
  # Set API key
  export ANTHROPIC_API_KEY=sk-ant-...
  # or
  export OPENROUTER_API_KEY=sk-or-...

  # Start dev server
  pnpm dev
\`;

export const CMD_GITHUB = "https://github.com/niclas-niclas/pi-agent-just\\n";

// File contents (generated from repo)
export const FILE_README = \`${escapeTemplate(readme)}\`;

export const FILE_PACKAGE_JSON = \`${escapeTemplate(JSON.stringify(
  {
    name: packageJson.name,
    version: packageJson.version,
    private: packageJson.private,
  },
  null,
  2
))}\`;
`;

fs.writeFileSync(outputFile, content);
console.log(`Generated ${outputFile}`);
